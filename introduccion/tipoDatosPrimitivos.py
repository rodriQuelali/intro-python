a = 10          # int
b = 3.14        # float
c = True        # bool
d = "Python"    # str
e = b"data"     # bytes
f = None        # NoneType

print(type(a))  # <class 'int'>
print(type(b))  # <class 'float'>
print(type(c))  # <class 'bool'>
print(type(d))  # <class 'str'>
print(type(e))  # <class 'bytes'>
print(type(f))  # <class 'NoneType'>